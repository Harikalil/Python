#Arithmetic operators
int_num1=int(input("Enter the Number1:"))
int_num2=int(input("Enter the Number2:"))
print(f'{int_num1}+{int_num2}={int_num1+int_num2}')
print(f'{int_num1}-{int_num2}={int_num1-int_num2}')
print(f'{int_num1}*{int_num2}={int_num1*int_num2}')
print(f'{int_num1}/{int_num2}={int_num1/int_num2}')#division shows quotient
print(f'{int_num1}//{int_num2}={int_num1//int_num2}')#floor Division
print(f'{int_num1}%{int_num2}={int_num1%int_num2}')#module shows remainder
print(f'{int_num1}**{int_num2}={int_num1**int_num2}')#expontent i.e., power