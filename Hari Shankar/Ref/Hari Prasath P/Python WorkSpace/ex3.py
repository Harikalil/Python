num1=int(input("Enter the Number1:"))
num2=int(input("Enter the Number2:"))
print(f'{num1}+{num2}={num1+num2}')
print(f'{num1}-{num2}={num1-num2}')
print(f'{num1}*{num2}={num1*num2}')
print(f'{num1}/{num2}={num1/num2}')
print(f'{num1}//{num2}={num1//num2}')
print(f'{num1}%{num2}={num1%num2}')
print(f'{num1}**{num2}={num1**num2}')